public class VariableAndConstant {
    public static void main(String[] args) {
        //Variable ia a named memory location that can change.
        int age=17;
        age = 2; //here value is changed.
        System.out.println(age);

        //Constant is a value that cannot change.
        final float PI=3.14159f;
        //  PI=111.2f;
        System.out.println(PI);
    }
}

//For Example: My age(variable.) changes yearly, but my birth year is constant, it never changes.