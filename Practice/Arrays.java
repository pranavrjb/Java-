public class Arrays {
    public static void main(String[] args) {
        //Arrays is a data stucture used to store multiple elements of the same types under a single variable name.
        //there are 2 types of arrays. one dimensional array $ multidimensional array
        int[] n={1,2,3,4,5};
        System.out.println(n[2]);
        int[][] n1={{1,2,3},{5,6,7}};
        System.out.println(n1[1][0]);
    }
}
